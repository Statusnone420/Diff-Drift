// Builds the engine coverage card (HTML) for the README.
//
//   npm run coverage:card
//
// Two panels, both computed — nothing hand-typed:
//   1. Coverage: engine eval cases counted from eval/cases/*.case.mjs,
//      bucketed by filename prefix into language families + dependency
//      manifests. The suite is a CI blocker, so main always passes 100%.
//   2. Quiet: per-repo active flags from the latest fp-replay run
//      (.eval/results/fp-replay/latest.json), split into security-rule
//      flags and dependency-change notices so the card can't hide either.
//
// Render to PNG with:
//   npm run coverage:capture
// which chains this script into capture-scorecard.mjs (same Playwright path
// the blind-agent scorecard uses).

import { readdirSync, readFileSync, mkdirSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const root = process.cwd();
const casesDir = resolve(root, "eval/cases");
const replayPath = resolve(root, ".eval/results/fp-replay/latest.json");
const outPath = resolve(root, "eval/benchmarks/coverage/coverage-card.html");

// ---- panel 1: coverage, counted from disk ----

const FAMILIES = [
  ["rust-", "Rust"],
  ["go-", "Go"],
  ["python-", "Python"],
  ["java-", "Java"],
  ["csharp-", "C#"],
  ["kotlin-", "Kotlin"],
  ["swift-", "Swift"],
  ["deps-", "Dependency manifests"],
];

const caseFiles = readdirSync(casesDir).filter((f) => f.endsWith(".case.mjs"));
const buckets = new Map([["JS/TS", 0]]);
for (const [, label] of FAMILIES) buckets.set(label, 0);
for (const f of caseFiles) {
  const fam = FAMILIES.find(([prefix]) => f.startsWith(prefix));
  const label = fam ? fam[1] : "JS/TS";
  buckets.set(label, buckets.get(label) + 1);
}
const total = caseFiles.length;
const sum = [...buckets.values()].reduce((a, b) => a + b, 0);
if (sum !== total) throw new Error(`bucket sum ${sum} != case count ${total}`);

const rows = [...buckets.entries()].sort((a, b) => b[1] - a[1]);
const maxCases = rows[0][1];

// ---- panel 2: quiet, read from the latest fp-replay run ----

let replay;
try {
  replay = JSON.parse(readFileSync(replayPath, "utf8"));
} catch {
  throw new Error(
    `missing ${replayPath} — run \`npm run eval:fp-replay\` first; the quiet panel only shows measured numbers`,
  );
}

const DEP_RULES = new Set([
  "Dependency version changed",
  "New dependency",
  "npm script changed",
  "Dependency not in lockfile",
]);

const targets = replay.targets.map((t) => {
  let dep = 0;
  let code = 0;
  for (const [rule, n] of Object.entries(t.byRule ?? {})) {
    if (DEP_RULES.has(rule)) dep += n;
    else code += n;
  }
  return { label: t.label, files: t.changedFiles, dep, code, flags: t.activeFlags };
});
const totFiles = targets.reduce((a, t) => a + t.files, 0);
const totDep = targets.reduce((a, t) => a + t.dep, 0);
const totCode = targets.reduce((a, t) => a + t.code, 0);
const totFlags = targets.reduce((a, t) => a + t.flags, 0);
if (totDep + totCode !== totFlags) {
  throw new Error(`flag split ${totDep}+${totCode} != total ${totFlags}`);
}
const maxFlags = Math.max(...targets.map((t) => t.flags), 1);
const replayDate = (replay.generatedAt ?? "").slice(0, 10);

// ---- render ----

const covRows = rows
  .map(([label, n]) => {
    const w = Math.max(4, Math.round((n / maxCases) * 100));
    return `      <div class="cov-row">
        <span class="cov-label">${label}</span>
        <div class="cov-track"><i style="width:${w}%"></i></div>
        <span class="cov-n">${n}</span>
      </div>`;
  })
  .join("\n");

const quietRows = targets
  .map((t) => {
    const codeW = Math.round((t.code / maxFlags) * 100);
    const depW = Math.round((t.dep / maxFlags) * 100);
    const bars =
      t.flags === 0
        ? `<span class="zero">0 — silent</span>`
        : `<i class="seg code" style="width:${codeW}%"></i><i class="seg dep" style="width:${depW}%"></i><span class="q-n">${t.flags}</span>`;
    return `      <div class="q-row">
        <span class="q-label">${t.label}</span>
        <span class="q-files">${t.files} files</span>
        <div class="q-bar">${bars}</div>
      </div>`;
  })
  .join("\n");

